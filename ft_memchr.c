void *memchr(const void *s, int c, size_t n)
{
    void *ptr;

    ptr = NULL;
    while(n)
    {
        if(*s == c)
        {
            ptr = s;
            return (ptr);
        }
        else
        {
            s++;
            n--;
        }
    }
    return (ptr);
}
